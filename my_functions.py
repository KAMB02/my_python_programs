def somme(x,y):
    '''cette fct permet de faire la somme 
    de deux nombre'''
    return x+y
def mult(x,y):
    '''cette fonct permet de faire le produit
    de deux nombre'''
    return x*y
def div(x,y):
    ''' cette fct effectue la division
    de deux nombre x par y'''
    a=x/y
    return a
def maxi(x,y):
    '''cette fct permet de trouver le max entre
    deux nombres'''
    if(x>y):
        return x
    else:
        return y

        